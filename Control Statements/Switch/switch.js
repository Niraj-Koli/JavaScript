const grade = "C";

switch (grade) {
    case "A":
        console.log("You have got 90+ marks");
        break;
    case "B":
        console.log("You have got 80+ marks");
        break;
    case "C":
        console.log("You have got 70+ marks");
        break;
    case "D":
        console.log("You have got 60+ marks");
        break;
    case "E":
        console.log("You have got 50+ marks");
        break;
    case "F":
        console.log("You have less than 50 marks");
        break;
    default:
        console.log("Not a valid grade");
        break;
}
